sayı= int(input ("Sayi Giriniz:"))
if sayı>0:
    if sayı%2==0:
        print(" Sayi pozitif Cifttir.")
    else :
        print(" Sayi poziti Tektir")
elif sayı<0:
    if sayı%2==0:
        print(" Sayi negatif Cidttir.")
    else :
        print(" Sayi negatif Tektir")
else:
    print ("Sayi Sifirdir")